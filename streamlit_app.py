def train_model(fight_data):
    """Train SVM model and evaluate with cross-validation"""

    features = ['power_diff', 'resistance_diff', 'ability_diff']
    X = fight_data[features].values
    y = fight_data['verdict_type'].values

    # Standardize
    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X)

    # Train model
    model = SVC(kernel='rbf', C=10, gamma='scale', probability=True, random_state=123)
    model.fit(X_scaled, y)

    # Predictions
    predictions = model.predict(X_scaled)
    accuracy = accuracy_score(y, predictions)

    # Cross-validation
    cv = StratifiedKFold(n_splits=min(5, len(np.unique(y))), shuffle=True, random_state=42)
    pipeline = make_pipeline(StandardScaler(), SVC(kernel='rbf', C=10, gamma='scale', random_state=123))
    cv_scores = cross_val_score(pipeline, X, y, cv=cv, scoring='accuracy')

    return model, scaler, X_scaled, y, predictions, accuracy, cv_scores

model, scaler, X_scaled, y, predictions, accuracy, cv_scores = train_model(fight_data)

# Create results dataframe
results_df = fight_data[['fight_id', 'opponent_1', 'opponent_2', 'verdict_type']].copy()
results_df['svm_prediction'] = predictions
results_df['agreement'] = results_df['verdict_type'] == results_df['svm_prediction']

