def create_confusion_matrix():
    cm = confusion_matrix(y, predictions, labels=model.classes_)
    color_map = {'KO': '#2E8B57', 'UD': '#DC143C', 'SD': '#FF8C00',
                 'MD': '#9370DB', 'RTD': '#FF69B4', 'TKO': '#4169E1'}
    fig = px.imshow(
        cm, x=model.classes_, y=model.classes_, text_auto=True,
        title="Confusion Matrix - SVM Predictions vs Actual",
        labels=dict(x="Predicted", y="Actual", color="Count"),
        color_continuous_scale='Blues'
    )
    fig.update_layout(height=500, title_x=0.5)
    return fig

def create_pca_visualization():
    pca = PCA(n_components=2)
    pca_result = pca.fit_transform(X_scaled)
    pca_df = pd.DataFrame(pca_result, columns=['PC1', 'PC2'])
    pca_df['verdict'] = y
    color_map = {'KO': '#2E8B57', 'UD': '#DC143C', 'SD': '#FF8C00',
                 'MD': '#9370DB', 'RTD': '#FF69B4', 'TKO': '#4169E1'}
    fig = px.scatter(
        pca_df, x='PC1', y='PC2', color='verdict',
        title="PCA Visualization - Feature Space Distribution",
        labels={'PC1': f'PC1 ({pca.explained_variance_ratio_[0]:.1%})',
               'PC2': f'PC2 ({pca.explained_variance_ratio_[1]:.1%})'},
        color_discrete_map=color_map
    )
    fig.update_layout(height=500, title_x=0.5)
    return fig

def create_feature_boxplots():
    feature_data = fight_data[['verdict_type', 'power_diff', 'resistance_diff', 'ability_diff']]
    feature_melted = feature_data.melt(id_vars=['verdict_type'], var_name='Feature', value_name='Difference')
    color_map = {'KO': '#2E8B57', 'UD': '#DC143C', 'SD': '#FF8C00',
                 'MD': '#9370DB', 'RTD': '#FF69B4', 'TKO': '#4169E1'}
    fig = px.box(
        feature_melted, x='verdict_type', y='Difference', color='verdict_type',
        facet_col='Feature', title="Feature Distributions by Verdict Type",
        labels={'verdict_type': 'Verdict', 'Difference': 'Value'},
        color_discrete_map=color_map
    )
    fig.update_layout(height=500, title_x=0.5)
    return fig

def create_decision_boundary():
    x_min, x_max = fight_data['power_diff'].min() - 10, fight_data['power_diff'].max() + 10
    y_min, y_max = fight_data['resistance_diff'].min() - 10, fight_data['resistance_diff'].max() + 10
    xx, yy = np.meshgrid(np.linspace(x_min, x_max, 100), np.linspace(y_min, y_max, 100))
    z_mean = fight_data['ability_diff'].mean()
    grid_points = np.c_[xx.ravel(), yy.ravel(), np.full(xx.ravel().shape, z_mean)]
    grid_scaled = scaler.transform(grid_points)
    Z = model.predict(grid_scaled)
    Z = Z.reshape(xx.shape)

    color_map = {'KO': '#2E8B57', 'UD': '#DC143C', 'SD': '#FF8C00',
                 'MD': '#9370DB', 'RTD': '#FF69B4', 'TKO': '#4169E1'}

    fig = go.Figure()

    for verdict in model.classes_:
        mask = (Z == verdict)
        if mask.any():
            fig.add_trace(go.Contour(
                z=mask.astype(int), x=np.linspace(x_min, x_max, 100), y=np.linspace(y_min, y_max, 100),
                contours=dict(showlabels=False, showlines=False), showscale=False,
                opacity=0.3, fillcolor=color_map.get(verdict, 'gray'), line=dict(width=0),
                name=f"{verdict} Region"
            ))

    for verdict in fight_data['verdict_type'].unique():
        subset = fight_data[fight_data['verdict_type'] == verdict]
        fig.add_trace(go.Scatter(
            x=subset['power_diff'], y=subset['resistance_diff'], mode='markers', name=verdict,
            marker=dict(size=10, color=color_map.get(verdict, 'gray'), opacity=0.8),
            text=subset['fight_id'],
            hovertemplate='Fight: %{text}<br>Power: %{x:.1f}<br>Resistance: %{y:.1f}<extra></extra>'
        ))

    fig.update_layout(
        title="SVM Decision Boundary (Ability fixed at mean)",
        xaxis_title="Power Difference (F1 - F2)",
        yaxis_title="Resistance Difference (F1 - F2)",
        height=500, title_x=0.5, showlegend=True
    )
    return fig

