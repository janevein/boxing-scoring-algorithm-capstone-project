def train_model(df):
    """Train SVM model using uploaded data"""
    try:
        # Map column names (handling your specific naming convention)
        df_renamed = df.copy()

        # Map power columns
        if 'opponent_1_estimated_punch_power' in df.columns:
            df_renamed['opponent_1_power'] = df['opponent_1_estimated_punch_power']
        if 'opponent_2_estimated_punch_power' in df.columns:
            df_renamed['opponent_2_power'] = df['opponent_2_estimated_punch_power']

        # Map resistance columns
        if 'opponent_1_estimated_punch_resistance' in df.columns:
            df_renamed['opponent_1_resistance'] = df['opponent_1_estimated_punch_resistance']
        if 'opponent_2_estimated_punch_resistance' in df.columns:
            df_renamed['opponent_2_resistance'] = df['opponent_2_estimated_punch_resistance']

        # Map ability columns
        if 'opponent_1_ability_to_take_punch' in df.columns:
            df_renamed['opponent_1_ability_to_take_punch'] = df['opponent_1_ability_to_take_punch']
        if 'opponent_2_ability_to_take_punch' in df.columns:
            df_renamed['opponent_2_ability_to_take_punch'] = df['opponent_2_ability_to_take_punch']

        # Map fighter names
        if 'opponent_1.x' in df.columns:
            df_renamed['opponent_1'] = df['opponent_1.x']
        if 'opponent_2.x' in df.columns:
            df_renamed['opponent_2'] = df['opponent_2.x']

        # Map fight_id
        if 'fight_id' in df.columns:
            df_renamed['fight_id'] = df['fight_id']

        # Check if we need to aggregate round-level data
        if 'round.x' in df_renamed.columns and df_renamed['round.x'].nunique() > 1:
            fight_data = df_renamed.groupby('fight_id').agg({
                'opponent_1': 'first',
                'opponent_2': 'first',
                'opponent_1_power': 'mean',
                'opponent_2_power': 'mean',
                'opponent_1_resistance': 'mean',
                'opponent_2_resistance': 'mean',
                'opponent_1_ability_to_take_punch': 'mean',
                'opponent_2_ability_to_take_punch': 'mean',
                'verdict': 'first'
            }).reset_index()
        else:
            fight_data = df_renamed.copy()

        # Calculate differences
        fight_data['power_diff'] = fight_data['opponent_1_power'] - fight_data['opponent_2_power']
        fight_data['resistance_diff'] = fight_data['opponent_1_resistance'] - fight_data['opponent_2_resistance']
        fight_data['ability_diff'] = fight_data['opponent_1_ability_to_take_punch'] - fight_data['opponent_2_ability_to_take_punch']

        # Extract verdict type
        def extract_verdict_type(verdict):
            if pd.isna(verdict):
                return None
            verdict_str = str(verdict).upper()
            if 'KO' in verdict_str:
                return 'KO'
            elif 'TKO' in verdict_str:
                return 'TKO'
            elif 'UD' in verdict_str:
                return 'UD'
            elif 'SD' in verdict_str:
                return 'SD'
            elif 'MD' in verdict_str:
                return 'MD'
            elif 'RTD' in verdict_str:
                return 'RTD'
            return None

        fight_data['verdict_type'] = fight_data['verdict'].apply(extract_verdict_type)
        fight_data = fight_data.dropna(subset=['verdict_type'])

        if len(fight_data) == 0:
            return None, None, None, "No valid verdict types found"

        # Train SVM
        features = ['power_diff', 'resistance_diff', 'ability_diff']
        X = fight_data[features]
        y = fight_data['verdict_type']

        scaler = StandardScaler()
        X_scaled = scaler.fit_transform(X)

        model = SVC(kernel='rbf', C=10, gamma='scale', probability=True, random_state=123)
        model.fit(X_scaled, y)

        # Make predictions
        predictions = model.predict(X_scaled)
        accuracy = accuracy_score(y, predictions)

        # Create results dataframe
        results_df = fight_data[['fight_id', 'opponent_1', 'opponent_2', 'verdict_type']].copy()
        results_df.columns = ['fight_id', 'opponent_1', 'opponent_2', 'actual_verdict']
        results_df['svm_prediction'] = predictions
        results_df['agreement'] = results_df['actual_verdict'] == results_df['svm_prediction']

        # Confusion matrix
        cm = confusion_matrix(y, predictions)
        fig_cm = px.imshow(
            cm,
            x=model.classes_,
            y=model.classes_,
            text_auto=True,
            title="Confusion Matrix - SVM Predictions vs Actual",
            labels=dict(x="Predicted", y="Actual"),
            color_continuous_scale='Blues'
        )
        fig_cm.update_layout(height=500)

        # PCA visualization
        pca = PCA(n_components=2)
        pca_result = pca.fit_transform(X_scaled)
        pca_df = pd.DataFrame(pca_result, columns=['PC1', 'PC2'])
        pca_df['verdict'] = y

        color_map = {
            'KO': '#2E8B57', 'TKO': '#4169E1', 'UD': '#DC143C',
            'SD': '#FF8C00', 'MD': '#9370DB', 'RTD': '#FF69B4'
        }

        fig_pca = px.scatter(
            pca_df, x='PC1', y='PC2', color='verdict',
            title="PCA Visualization of Fight Data",
            labels={'PC1': f'PC1 ({pca.explained_variance_ratio_[0]:.1%})',
                   'PC2': f'PC2 ({pca.explained_variance_ratio_[1]:.1%})'},
            color_discrete_map=color_map
        )
        fig_pca.update_layout(height=500)

        return model, scaler, fight_data, results_df, fig_cm, fig_pca, accuracy

    except Exception as e:
        return None, None, None, None, None, None, str(e)

def predict_fight(model, scaler, power_diff, resistance_diff, ability_diff):
    """Predict verdict for a new fight"""
    if model is None:
        return None, None

    input_features = np.array([[power_diff, resistance_diff, ability_diff]])
    input_scaled = scaler.transform(input_features)

    prediction = model.predict(input_scaled)[0]
    probabilities = model.predict_proba(input_scaled)[0]
    confidence = max(probabilities)

    # Color mapping
    color_map = {
        'KO': '#2E8B57', 'TKO': '#4169E1', 'UD': '#DC143C',
        'SD': '#FF8C00', 'MD': '#9370DB', 'RTD': '#FF69B4'
    }

    # Probability bar chart
    prob_df = pd.DataFrame({
        'Verdict': model.classes_,
        'Probability': probabilities
    })

    fig = px.bar(
        prob_df,
        x='Verdict',
        y='Probability',
        title=f"Prediction: {prediction} (Confidence: {confidence:.1%})",
        color='Verdict',
        color_discrete_map=color_map,
        text=prob_df['Probability'].apply(lambda x: f'{x:.1%}')
    )
    fig.update_traces(textposition='outside')
    fig.update_layout(yaxis_tickformat='.0%', height=400)

    return prediction, confidence, fig

