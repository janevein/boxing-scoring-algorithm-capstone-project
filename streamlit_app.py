@st.cache_data
def load_and_process_data():
    """Load audit_data.csv and process to fight level"""
    df = pd.read_csv('audit_data.csv')

    def extract_verdict(verdict_text):
        if pd.isna(verdict_text):
            return None
        v = str(verdict_text).upper()
        if 'KO' in v:
            return 'KO'
        elif 'TKO' in v:
            return 'TKO'
        elif 'UD' in v:
            return 'UD'
        elif 'SD' in v:
            return 'SD'
        elif 'MD' in v:
            return 'MD'
        elif 'RTD' in v:
            return 'RTD'
        return None

    # Aggregate to fight level
    fight_data = df.groupby('fight_id').agg({
        'opponent_1.x': 'first',
        'opponent_2.x': 'first',
        'opponent_1_estimated_punch_power': 'mean',
        'opponent_2_estimated_punch_power': 'mean',
        'opponent_1_estimated_punch_resistance': 'mean',
        'opponent_2_estimated_punch_resistance': 'mean',
        'opponent_1_ability_to_take_punch': 'mean',
        'opponent_2_ability_to_take_punch': 'mean',
        'verdict': 'first'
    }).reset_index()

    fight_data = fight_data.rename(columns={
        'opponent_1.x': 'opponent_1',
        'opponent_2.x': 'opponent_2'
    })

    # Calculate differences
    fight_data['power_diff'] = fight_data['opponent_1_estimated_punch_power'] - fight_data['opponent_2_estimated_punch_power']
    fight_data['resistance_diff'] = fight_data['opponent_1_estimated_punch_resistance'] - fight_data['opponent_2_estimated_punch_resistance']
    fight_data['ability_diff'] = fight_data['opponent_1_ability_to_take_punch'] - fight_data['opponent_2_ability_to_take_punch']

    # Extract verdict
    fight_data['verdict_type'] = fight_data['verdict'].apply(extract_verdict)
    fight_data = fight_data.dropna(subset=['verdict_type'])

    return fight_data

# Load data
fight_data = load_and_process_data()
