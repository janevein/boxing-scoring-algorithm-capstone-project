def manual_cross_validation(X_list, y_list, n_folds=5):
    """
    Manual cross-validation using pure Python lists and indices.
    This completely avoids pandas/numpy indexing issues.
    """
    n_samples = len(X_list)
    indices = list(range(n_samples))

    # Shuffle indices
    np.random.seed(42)
    np.random.shuffle(indices)

    # Create folds
    fold_size = n_samples // n_folds
    folds = []
    for i in range(n_folds):
        start = i * fold_size
        end = (i + 1) * fold_size if i < n_folds - 1 else n_samples
        folds.append(indices[start:end])

    cv_scores = []

    for i in range(n_folds):
        # Get test indices for this fold
        test_idx = folds[i]

        # Get train indices (all other folds)
        train_idx = []
        for j in range(n_folds):
            if j != i:
                train_idx.extend(folds[j])

        # Prepare training data
        X_train = [X_list[idx] for idx in train_idx]
        y_train = [y_list[idx] for idx in train_idx]

        # Prepare test data
        X_test = [X_list[idx] for idx in test_idx]
        y_test = [y_list[idx] for idx in test_idx]

        # Convert to numpy arrays
        X_train_np = np.array(X_train, dtype=np.float32)
        X_test_np = np.array(X_test, dtype=np.float32)

        # Standardize
        scaler = StandardScaler()
        X_train_scaled = scaler.fit_transform(X_train_np)
        X_test_scaled = scaler.transform(X_test_np)

        # Train and predict
        model = SVC(kernel='rbf', C=10, gamma='scale', random_state=123)
        model.fit(X_train_scaled, y_train)
        y_pred = model.predict(X_test_scaled)

        # Calculate accuracy
        correct = sum(1 for a, b in zip(y_test, y_pred) if a == b)
        acc = correct / len(y_test)
        cv_scores.append(acc)

    return np.mean(cv_scores), np.std(cv_scores)

